
defmodule DictionaryExplanation do

  # 딕셔너리
  # 딕셔너리는 키와 값을 연결해 데이터를 저장하는 자료형을 말한다

  # 딕셔너리 선택 방법
  # 내용을 기준으로 패턴 매칭을 하고 싶은가?    => 맵 (ex :name이라는 키가 있는 경우를 패턴 매칭하고 싶은가?)
  # 같은 키로 여러 항목을 저장해야 하는가?      => 키워드 리스트
  # 요소들의 순서가 보장되어야 하는가?          => 키워드 리스트
  # 필드가 정해져 있나?                        => 구조체 (즉, 데이터의 구조가 항상 동일한가?)
  # 어느 것에도 해당하지 않는 경우              => 맵
end
