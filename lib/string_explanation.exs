
# 문자열 리터럴
# 엘릭서에는 두 종류에 문자열이 있다
# 하나는 작은따옴표(single quotation)로 감싼 것이고 다른 하나는 큰따옴표(double quotation)로 감싼 것이다
# 둘은 내부 구현이 매우 다르지만 공통점도 많다
# 양쪽 모두 다음과 같은 특징이 있다
# - UTF-8 인코딩된 문자를 저장할 수 있다
# - 이스케이프 문자를 포함할 수 있다
# - #{...} 문법으로 엘릭서 표현식을 삽입할 수 있다
# - 특별한 의미를 가진 문자는 백슬래시(\)를 사용해 이스케이프할 수 있다 (ex \' 등)
